--Hack to fix 914
--You can have a command call this, or stick it in your breach mapconfig using timers, delay should be around 15 seconds
function Fix914()
	--Only site19 is supported
	if game.GetMap() != "gm_site19_v6" then return end
	for k,v in pairs(ents.GetAll()) do
		--Basically, the button is a brush ent and *216 is its model. *216 should be unique to the trigger, and this is the best way we have to indentify it
		if v and IsValid(v) and v:GetClass() == "func_button" and v.GetModel and v:GetModel() == "*229" then
			v:Fire("Unlock")
			--If we were to just PressOut again, then 914 would go again, so we disable the logic relay
			for x,y in pairs(ents.GetAll()) do
				if y and IsValid(y) and y:GetClass() == "logic_relay" and y.GetName and y:GetName() == "914_relay" then
					y:Fire("Disable")
					--So we can enable it next frame
					local e = y
					timer.Simple(0.1, function ()
						if e and IsValid(e) then
							--Scope pyramid, idc
							e:Fire("Enable")
						end
					end)
				end
			end
			--This part presses the button, resetting it to the old position.
			v:Fire("PressOut")
			timer.Simple(0, function ()
				for a,b in pairs(ents.GetAll()) do
					--This unlocks the selector and the trigger, because the PressOut will also lock these two.
					if v.GetModel and (v:GetModel() == "*229" or v:GetModel() == "*221") then
						v:Fire("Unlock")
					end
				end
			end)
		end
	end
end


